public class LogicOperators {
    
    public static void main(String[] args) {

        if (10 == 10 && 100 < 20) {
            System.out.println("True");
        } else {
            System.out.println("False");
        }

        if (10 == 10 || 100 < 20) {
            System.out.println("True");
        } else {
            System.out.println("False");
        }

    }

}
