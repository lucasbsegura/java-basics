public class SwitchCase {
    
    public static void main(String[] args) {

        int opcoes = 1;

        switch (opcoes) {
            case 1: 
                System.out.println("Option 1");
                break;
            case 2: 
                System.out.println("Option 2");
                break;
            default: 
                System.out.println("Default");
                break;
        }

    }

}
