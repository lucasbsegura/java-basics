public class Arrays {
    
    public static void main(String[] args) {
        
        String[] names_list = {"Lucas", "Claudio", "Jonas"};
        for (int i = 0; i < names_list.length; i++) {
            System.out.println(names_list[i]);
        }

        int[] numbers_list = { 0, 7, 4, 9, 34 };
        for (int i = 0; i < numbers_list.length; i++) {
            System.out.println(numbers_list[i]);
        }
    }

}
